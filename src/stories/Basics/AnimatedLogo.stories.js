const cycle = r =>
  `translate3d(0, ${15 * Math.sin(r + (2 * Math.PI) / 1.6)}px, 0)`;

function AnimatedLogo() {
  const { radians } = useSpring({
    to: async next => {
      while (1) await next({ radians: 2 * Math.PI });
    },
    from: { radians: 0 },
    config: { duration: 3500 },
    reset: true
  });

  const { x } = useSpring({
    from: { x: 0 },
    to: async next => {
      while (1) await next({ x: 1 });
    },
    config: { duration: 3500 },
    reset: true
  });

  return (
    <div>
      <Wrapper>
        <CenterLogo style={{ transform: radians.interpolate(cycle) }}>
          <Logo />
        </CenterLogo>
        <animated.svg
          style={{
            transform: x
              .interpolate({
                range: [0, 0.1, 0.66, 1],
                output: [0.1, 0.08, 0.13, 0.1]
              })
              .interpolate(x => `scale(${x})`)
          }}
          height="100"
          viewBox="0 0 789 130"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
        >
          <ellipse
            cx="394.5"
            cy="64.502"
            rx="394.5"
            ry="64.5"
            fill="black"
            fill-opacity="0.19"
          />
        </animated.svg>
      </Wrapper>
    </div>
  );
}

